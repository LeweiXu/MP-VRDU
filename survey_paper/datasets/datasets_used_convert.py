#!/usr/bin/env python3
r"""
datasets_used_convert.py
------------------------
Emit a LaTeX table mirroring ``datasets_summary.csv`` row-for-row,
with three columns:

    Dataset | Type | Models

The Dataset and Type columns are taken verbatim from
``datasets_summary.csv``.  The Models column is built from
``datasets_used.csv`` (which has the form: Dataset, Type, Paper,
Usage) by collecting every distinct paper that uses the dataset.  If a
dataset is not referenced in ``datasets_used.csv``, the Models cell
shows ``-``.

Usage:
    python3 datasets_used_convert.py [summary.csv] [used.csv] [output.tex]

Defaults:
    summary.csv  -> datasets_summary.csv
    used.csv     -> datasets_used.csv
    output.tex   -> datasets_used.tex
"""

import re
import sys
from pathlib import Path
import pandas as pd

# -- CLI args -----------------------------------------------------------------
BASE_DIR = Path(__file__).resolve().parent
SUMMARY_CSV = sys.argv[1] if len(sys.argv) > 1 else str(BASE_DIR / "datasets_summary.csv")
USED_CSV = sys.argv[2] if len(sys.argv) > 2 else str(BASE_DIR / "datasets_used.csv")
OUTPUT_TEX = sys.argv[3] if len(sys.argv) > 3 else str(BASE_DIR / "datasets_used.tex")

PREAMBLE = r"""% -----------------------------------------------------------------------------
% Auto-generated by datasets_used_convert.py -- do NOT edit manually.
% Re-generate:  python3 datasets_used_convert.py datasets_summary.csv datasets_used.csv datasets_used.tex
%
% Required packages (add to your preamble):
%   \usepackage{booktabs}
%   \usepackage{array}
%   \usepackage{xcolor}
%   \usepackage{colortbl}
%   \usepackage{caption}
%   \usepackage{natbib}      % for \citeyear  (or use biblatex)
% -----------------------------------------------------------------------------

"""

TYPE_NORMALISE = {
    "B": "BM",
    "Benchmark": "BM",
    "BENCHMARK": "BM",
    "PT+BM": "PT+BM",
    "PT+IT/FT": "PT+IT/FT",
    "IT/FT": "IT/FT",
    "BM": "BM",
    "PT": "PT",
}


def escape_latex(val):
    if val is None:
        return "--"
    val = str(val).strip()
    if not val or val == "-":
        return "--"
    for char, repl in [
        ("\\", r"\textbackslash{}"),
        ("&", r"\&"),
        ("%", r"\%"),
        ("$", r"\$"),
        ("#", r"\#"),
        ("_", r"\_"),
        ("{", r"\{"),
        ("}", r"\}"),
        ("~", r"\textasciitilde{}"),
        ("^", r"\textasciicircum{}"),
    ]:
        val = val.replace(char, repl)
    return val


def extract_year(bibtex_key):
    if not bibtex_key or pd.isna(bibtex_key):
        return None
    m = re.search(r"(20\d{2}|19\d{2})", str(bibtex_key))
    return m.group(1) if m else None


def lookup_key(name):
    """Normalised matching key for a dataset name."""
    if not name or pd.isna(name):
        return ""
    s = str(name)
    s = s.replace("†", "").replace("†", "")
    # Drop trailing parenthetical aliases, e.g. "WikiTableQuestions (WTQ)".
    s = re.sub(r"\s*\([^)]*\)\s*$", "", s)
    return re.sub(r"[^a-z0-9]", "", s.lower())


def make_dataset_cell(name, bibtex):
    r"""Return ``Name~(\citeyear{key})`` (no bold) or just the name."""
    cell = escape_latex(name)
    if not bibtex or pd.isna(bibtex):
        return cell
    bibtex = str(bibtex).strip()
    if not bibtex:
        return cell
    year = extract_year(bibtex)
    if year:
        return rf"{cell}~(\citeyear{{{bibtex}}})"
    return rf"{cell}~\cite{{{bibtex}}}"


def build_paper_lookup(used_csv_path):
    r"""
    Read ``datasets_used.csv`` and build {normalised_dataset_name -> ordered
    unique list of papers}.  The used CSV has columns ``Dataset, Type, Paper, Usage``.
    """
    df = pd.read_csv(used_csv_path)
    if "Dataset" not in df.columns or "Paper" not in df.columns:
        raise ValueError(
            "datasets_used.csv must have 'Dataset' and 'Paper' columns"
        )
    lookup = {}
    for _, row in df.iterrows():
        ds = row.get("Dataset")
        paper = row.get("Paper")
        if pd.isna(ds) or pd.isna(paper):
            continue
        key = lookup_key(ds)
        if not key:
            continue
        papers = lookup.setdefault(key, [])
        paper_str = str(paper).strip()
        # Strip any stray dagger / parenthetical annotations.
        paper_str = re.sub(r"\s*\([^)]*\)", "", paper_str).strip()
        paper_str = paper_str.replace("†", "").replace("†", "").strip()
        if paper_str and paper_str not in papers:
            papers.append(paper_str)
    return lookup


def main():
    summary = pd.read_csv(SUMMARY_CSV)
    if "Name" not in summary.columns:
        raise ValueError("datasets_summary.csv missing 'Name' column")
    if "Type" not in summary.columns:
        raise ValueError("datasets_summary.csv missing 'Type' column")

    paper_lookup = build_paper_lookup(USED_CSV)

    rows = []
    for _, row in summary.iterrows():
        name = row.get("Name")
        if pd.isna(name):
            continue
        bibtex = row.get("Bibtex")
        type_raw = row.get("Type", "")
        type_val = TYPE_NORMALISE.get(str(type_raw).strip(), str(type_raw).strip())
        key = lookup_key(name)
        papers = paper_lookup.get(key, [])
        if papers:
            models_cell = escape_latex(", ".join(papers))
        else:
            models_cell = "--"
        rows.append({
            "dataset_cell": make_dataset_cell(name, bibtex),
            "type_cell": escape_latex(type_val),
            "models_cell": models_cell,
        })

    # The Models column is a fixed-width p{} entry so long lists wrap
    # across multiple lines instead of overflowing the page width.  Drops
    # adjustbox (since adjustbox uniformly scales, it cannot wrap text).
    col_spec = r"l c >{\raggedright\arraybackslash}p{0.70\textwidth}"
    header = (
        r"\textbf{Dataset} & \textbf{Type} & \textbf{Models / Papers using it}"
    )

    body_lines = [
        "  " + " & ".join([r["dataset_cell"], r["type_cell"], r["models_cell"]]) + r" \\"
        for r in rows
    ]
    body = "\n".join(body_lines)

    caption = (
        r"Datasets referenced in the MP-VRDU survey and the models that use them. "
        r"\textbf{Type}: PT (Pretraining), IT/FT (Instruction Tuning / Fine-Tuning), "
        r"BM (Benchmark), and combinations (matches Table~\ref{tab:dataset_survey}). "
        r"\textbf{Models}: papers from the surveyed corpus that pretrain on, "
        r"fine-tune on, or evaluate on the dataset; ``--'' indicates the dataset "
        r"is not used by any paper in our corpus."
    )

    table_lines = [
        r"% -- tab:datasets_used ------------------------------------------------",
        r"\begin{table*}[t]",
        r"\centering",
        r"\scriptsize",
        r"\captionsetup{font=small}",
        r"\setlength{\tabcolsep}{3pt}",
        r"\renewcommand{\arraystretch}{1.1}",
        rf"\begin{{tabular}}{{{col_spec}}}",
        r"\toprule",
        rf"{header} \\",
        r"\midrule",
        "",
        body,
        "",
        r"\bottomrule",
        r"\end{tabular}",
        rf"\caption{{{caption}}}",
        r"\label{tab:datasets_used}",
        r"\end{table*}",
        "",
    ]

    with open(OUTPUT_TEX, "w", encoding="utf-8") as f:
        f.write(PREAMBLE)
        f.write("\n".join(table_lines))
    print(f"OK  Wrote {OUTPUT_TEX}  ({len(rows)} rows)")


if __name__ == "__main__":
    main()
