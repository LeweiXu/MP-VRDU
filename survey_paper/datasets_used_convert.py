#!/usr/bin/env python3
r"""
datasets_used_convert.py
------------------------
Reads datasets/datasets_used.csv and produces two LaTeX tables:

  1. datasets/contamination_table.tex
       Top-N datasets by total appearance count, with PT/FT/BM column counts
       and the FT∩BM overlap (papers that use the dataset in BOTH fine-tuning
       and benchmarking — the contamination signal flagged in §8.4).
       Intended for the main body next to the contamination discussion.

  2. datasets/datasets_used.tex
       Full per-dataset usage table, organised by role (PT/FT/BM), listing
       every surveyed paper that uses each dataset. Intended for the appendix.

Usage:
    python3 datasets_used_convert.py
        [datasets/datasets_used.csv] [datasets/contamination_table.tex]
        [datasets/datasets_used.tex]
"""

import sys
from collections import defaultdict
from pathlib import Path

import pandas as pd

BASE_DIR = Path(__file__).resolve().parent
INPUT_CSV = sys.argv[1] if len(sys.argv) > 1 else str(BASE_DIR / "datasets" / "datasets_used.csv")
INPUT_SUMMARY_CSV = str(BASE_DIR / "datasets" / "datasets_summary.csv")
OUTPUT_CONTAM = sys.argv[2] if len(sys.argv) > 2 else str(BASE_DIR / "datasets" / "contamination_table.tex")
OUTPUT_FULL = sys.argv[3] if len(sys.argv) > 3 else str(BASE_DIR / "datasets" / "datasets_used.tex")

TOP_N = 12  # number of datasets in the main-body contamination table
MIN_PAPERS_FOR_APPENDIX = 2  # appendix excludes datasets used by only one paper

ROLE_ORDER = ["Pretraining", "Fine-Tuning", "Benchmarking"]
ROLE_SHORT = {"Pretraining": "PT", "Fine-Tuning": "FT", "Benchmarking": "BM"}

# Aliases mapping names that appear in datasets_used.csv to their canonical
# entry in datasets_summary.csv. Add new entries here when a paper uses a
# subset/variant name (e.g. DocGenome12K refers to the same DocGenome corpus).
DATASET_ALIASES = {
    "DocGenome12K": "DocGenome",
}

LATEX_ESCAPES = [
    ("\\", r"\textbackslash{}"),
    ("&", r"\&"),
    ("%", r"\%"),
    ("$", r"\$"),
    ("#", r"\#"),
    ("_", r"\_"),
    ("{", r"\{"),
    ("}", r"\}"),
    ("~", r"\textasciitilde{}"),
    ("^", r"\textasciicircum{}"),
]


def latex_escape(text: str) -> str:
    if pd.isna(text):
        return ""
    s = str(text).strip()
    for char, repl in LATEX_ESCAPES:
        s = s.replace(char, repl)
    return s


def build_index(df: pd.DataFrame):
    """Return dict[dataset][role] = sorted unique list of papers."""
    index = defaultdict(lambda: defaultdict(set))
    for _, row in df.iterrows():
        dataset = str(row["Dataset"]).strip()
        # Apply alias mapping so DocGenome12K → DocGenome, etc.
        dataset = DATASET_ALIASES.get(dataset, dataset)
        role = str(row["Type"]).strip()
        paper = str(row["Paper"]).strip()
        if not dataset or not role or not paper:
            continue
        index[dataset][role].add(paper)
    return {d: {r: sorted(p, key=str.lower) for r, p in roles.items()} for d, roles in index.items()}


def load_summary_whitelist(path: str):
    """Return set of canonical dataset names from datasets_summary.csv."""
    df = pd.read_csv(path)
    return {str(name).strip() for name in df["Name"].dropna()}


def unique_papers_across_roles(roles_dict) -> int:
    """Count distinct papers across all roles for a dataset."""
    seen = set()
    for papers in roles_dict.values():
        seen.update(papers)
    return len(seen)


def build_contamination_table(index, top_n=TOP_N) -> str:
    rows = []
    for dataset, roles in index.items():
        pt = set(roles.get("Pretraining", []))
        ft = set(roles.get("Fine-Tuning", []))
        bm = set(roles.get("Benchmarking", []))
        total = len(pt) + len(ft) + len(bm)
        rows.append({
            "dataset": dataset,
            "pt": len(pt),
            "ft": len(ft),
            "bm": len(bm),
            "overlap": len(ft & bm),
            "total": total,
        })

    rows.sort(key=lambda r: (-r["total"], r["dataset"].lower()))
    top = rows[:top_n]

    body_lines = []
    for r in top:
        body_lines.append(
            f"  {latex_escape(r['dataset'])} & {r['pt']} & {r['ft']} & {r['bm']} & {r['overlap']} \\\\"
        )

    caption = (
        r"Top " + str(top_n) + r" datasets by total appearance count across the surveyed corpus, "
        r"with usage counts broken down by role. \textbf{FT $\cap$ BM} counts the papers that use "
        r"the dataset in \emph{both} fine-tuning and benchmarking, illustrating the contamination "
        r"loop discussed in \S\ref{sec:chal-sup}. Sorted by total appearance count."
    )

    return "\n".join([
        r"% ── tab:dataset_contamination ────────────────────────────────────────────",
        r"% Auto-generated by datasets_used_convert.py — do NOT edit manually.",
        r"\begin{table}[t]",
        r"\centering",
        r"\small",
        r"\setlength{\tabcolsep}{6pt}",
        r"\begin{tabular}{lrrrr}",
        r"\toprule",
        r"\textbf{Dataset} & \textbf{PT} & \textbf{FT} & \textbf{BM} & \textbf{FT $\cap$ BM} \\",
        r"\midrule",
        *body_lines,
        r"\bottomrule",
        r"\end{tabular}",
        r"\caption{" + caption + r"}",
        r"\label{tab:dataset_contamination}",
        r"\end{table}",
        "",
    ])


def build_full_table(index, whitelist, min_papers=MIN_PAPERS_FOR_APPENDIX) -> str:
    # Filter the index: keep only datasets that (a) appear in the curated
    # summary CSV and (b) are used by at least `min_papers` distinct papers.
    filtered_index = {
        dataset: roles
        for dataset, roles in index.items()
        if dataset in whitelist and unique_papers_across_roles(roles) >= min_papers
    }

    # Sanity log: which whitelist datasets were dropped because they appear in
    # only one paper in the usage CSV?
    dropped_singletons = sorted(
        dataset for dataset, roles in index.items()
        if dataset in whitelist and unique_papers_across_roles(roles) < min_papers
    )
    if dropped_singletons:
        print(f"   (dropped single-paper whitelist entries: {', '.join(dropped_singletons)})")

    missing_from_used = sorted(whitelist - set(index.keys()))
    if missing_from_used:
        print(f"   (whitelist entries with no usage records: {', '.join(missing_from_used)})")

    role_blocks = []
    for role in ROLE_ORDER:
        rows = [
            (dataset, roles[role])
            for dataset, roles in filtered_index.items()
            if role in roles and roles[role]
        ]
        rows.sort(key=lambda x: x[0].lower())
        if not rows:
            continue
        role_blocks.append((role, rows))

    lines = [
        r"% ── tab:datasets_used ───────────────────────────────────────────────────",
        r"% Auto-generated by datasets_used_convert.py — do NOT edit manually.",
        r"\begingroup",
        r"\footnotesize",
        r"\setlength{\tabcolsep}{4pt}",
        r"\renewcommand{\arraystretch}{1.05}",
        r"\begin{longtable}{p{32mm} p{120mm}}",
        r"\caption{Per-dataset usage across the surveyed corpus, grouped by role. "
        r"Each row lists the surveyed papers that use the dataset for that role. "
        r"Datasets used for multiple roles appear once per role.}"
        r" \label{tab:datasets_used}\\",
        r"\toprule",
        r"\textbf{Dataset} & \textbf{Papers} \\",
        r"\midrule",
        r"\endfirsthead",
        r"\multicolumn{2}{c}{\textit{Table~\ref{tab:datasets_used} continued from previous page}}\\",
        r"\toprule",
        r"\textbf{Dataset} & \textbf{Papers} \\",
        r"\midrule",
        r"\endhead",
        r"\midrule",
        r"\multicolumn{2}{r}{\textit{Continued on next page}}\\",
        r"\endfoot",
        r"\bottomrule",
        r"\endlastfoot",
    ]

    for idx, (role, rows) in enumerate(role_blocks):
        if idx > 0:
            lines.append(r"\midrule")
        lines.append(
            rf"\multicolumn{{2}}{{l}}{{\textbf{{{role} ({ROLE_SHORT[role]})}}}} \\"
        )
        lines.append(r"\midrule")
        for dataset, papers in rows:
            papers_str = ", ".join(latex_escape(p) for p in papers)
            lines.append(
                f"  {latex_escape(dataset)} & {papers_str} \\\\"
            )

    lines.append(r"\end{longtable}")
    lines.append(r"\endgroup")
    lines.append("")
    return "\n".join(lines)


def main():
    df = pd.read_csv(INPUT_CSV)
    df = df.dropna(subset=["Dataset", "Type", "Paper"])

    index = build_index(df)
    whitelist = load_summary_whitelist(INPUT_SUMMARY_CSV)

    contam_tex = build_contamination_table(index)
    full_tex = build_full_table(index, whitelist)

    with open(OUTPUT_CONTAM, "w", encoding="utf-8") as f:
        f.write(contam_tex)
    print(f"✓  Wrote {OUTPUT_CONTAM}")

    with open(OUTPUT_FULL, "w", encoding="utf-8") as f:
        f.write(full_tex)
    print(f"✓  Wrote {OUTPUT_FULL}")


if __name__ == "__main__":
    main()
