#!/usr/bin/env python3
r"""
datasets_used_convert.py
------------------------
Emit a LaTeX table mirroring ``datasets/datasets_summary.csv`` row-for-row,
with three columns:

    Dataset | Type | Models

Dataset, Bibtex and Type are taken directly from ``datasets_summary.csv``.
The Models column is built by matching each summary Name against the
``Dataset`` column of ``datasets_used.csv`` and joining the unique Paper
values with commas.  Datasets with no matches show ``-``.

Usage:
    python3 datasets_used_convert.py [summary.csv] [used.csv] [output.tex]
"""

import re
import sys
from pathlib import Path

import pandas as pd

BASE_DIR = Path(__file__).resolve().parent
DEFAULT_SUMMARY = BASE_DIR / "datasets" / "datasets_summary.csv"
DEFAULT_USED = BASE_DIR / "datasets" / "datasets_used.csv"
DEFAULT_OUTPUT = BASE_DIR / "datasets" / "datasets_used.tex"

SUMMARY_CSV = sys.argv[1] if len(sys.argv) > 1 else str(DEFAULT_SUMMARY)
USED_CSV = sys.argv[2] if len(sys.argv) > 2 else str(DEFAULT_USED)
OUTPUT_TEX = sys.argv[3] if len(sys.argv) > 3 else str(DEFAULT_OUTPUT)

PREAMBLE = r"""% -----------------------------------------------------------------------------
% Auto-generated by datasets_used_convert.py -- do NOT edit manually.
% Re-generate:  python3 datasets_used_convert.py
%
% Required packages:
%   \usepackage{booktabs}
%   \usepackage{array}
%   \usepackage{caption}
%   \usepackage{natbib}      % for \citeyear (or biblatex)
% -----------------------------------------------------------------------------

"""


def escape_latex(val):
    if val is None or (isinstance(val, float) and pd.isna(val)):
        return ""
    s = str(val).strip()
    if not s:
        return ""
    for ch, repl in [
        ("\\", r"\textbackslash{}"),
        ("&", r"\&"),
        ("%", r"\%"),
        ("$", r"\$"),
        ("#", r"\#"),
        ("_", r"\_"),
        ("{", r"\{"),
        ("}", r"\}"),
        ("~", r"\textasciitilde{}"),
        ("^", r"\textasciicircum{}"),
    ]:
        s = s.replace(ch, repl)
    return s


def norm_name(name):
    """Normalised matching key for a dataset name."""
    if name is None or (isinstance(name, float) and pd.isna(name)):
        return ""
    s = re.sub(r"\s*\([^)]*\)\s*$", "", str(name))  # drop trailing "(alias)"
    return re.sub(r"[^a-z0-9]", "", s.lower())


def make_dataset_cell(name, bibtex):
    cell = escape_latex(name)
    if bibtex is None or (isinstance(bibtex, float) and pd.isna(bibtex)):
        return cell
    key = str(bibtex).strip()
    if not key:
        return cell
    return rf"{cell}~\citeyear{{{key}}}"


def build_used_lookup(used_csv_path):
    """{normalised_dataset_name -> ordered unique list of papers}."""
    df = pd.read_csv(used_csv_path)
    for col in ("Dataset", "Paper"):
        if col not in df.columns:
            raise ValueError(f"{used_csv_path} missing '{col}' column")
    lookup = {}
    for _, row in df.iterrows():
        key = norm_name(row.get("Dataset"))
        paper = row.get("Paper")
        if not key or paper is None or (isinstance(paper, float) and pd.isna(paper)):
            continue
        paper = str(paper).strip()
        if not paper:
            continue
        papers = lookup.setdefault(key, [])
        if paper not in papers:
            papers.append(paper)
    return lookup


def main():
    summary = pd.read_csv(SUMMARY_CSV)
    for col in ("Name", "Bibtex", "Type"):
        if col not in summary.columns:
            raise ValueError(f"{SUMMARY_CSV} missing '{col}' column")

    used = build_used_lookup(USED_CSV)

    rows = []
    for _, row in summary.iterrows():
        name = row["Name"]
        if pd.isna(name):
            continue
        papers = used.get(norm_name(name), [])
        models_cell = escape_latex(", ".join(papers)) if papers else "-"
        rows.append({
            "dataset_cell": make_dataset_cell(name, row.get("Bibtex")),
            "type_cell": escape_latex(row.get("Type")),
            "models_cell": models_cell,
        })

    col_spec = r"l c >{\raggedright\arraybackslash}p{0.70\textwidth}"
    header = r"\textbf{Dataset} & \textbf{Type} & \textbf{Models / Papers using it}"
    body = "\n".join(
        "  " + " & ".join([r["dataset_cell"], r["type_cell"], r["models_cell"]]) + r" \\"
        for r in rows
    )

    caption = (
        r"Datasets referenced in the MP-VRDU survey and the models that use them. "
        r"\textbf{Type}: PT (Pretraining), IT/FT (Instruction Tuning / Fine-Tuning), "
        r"BM/B (Benchmark), and combinations (matches Table~\ref{tab:dataset_survey}). "
        r"\textbf{Models}: papers from the surveyed corpus that pretrain on, "
        r"fine-tune on, or evaluate on the dataset; ``-'' indicates the dataset "
        r"is not used by any paper in our corpus."
    )

    table_lines = [
        r"% -- tab:datasets_used ------------------------------------------------",
        r"\begin{table*}[t]",
        r"\centering",
        r"\scriptsize",
        r"\captionsetup{font=small}",
        r"\setlength{\tabcolsep}{3pt}",
        r"\renewcommand{\arraystretch}{1.1}",
        rf"\begin{{tabular}}{{{col_spec}}}",
        r"\toprule",
        rf"{header} \\",
        r"\midrule",
        "",
        body,
        "",
        r"\bottomrule",
        r"\end{tabular}",
        rf"\caption{{{caption}}}",
        r"\label{tab:datasets_used}",
        r"\end{table*}",
        "",
    ]

    with open(OUTPUT_TEX, "w", encoding="utf-8") as f:
        f.write(PREAMBLE)
        f.write("\n".join(table_lines))
    print(f"OK  Wrote {OUTPUT_TEX}  ({len(rows)} rows)")


if __name__ == "__main__":
    main()
