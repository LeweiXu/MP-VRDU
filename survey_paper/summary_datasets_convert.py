#!/usr/bin/env python3
r"""
summary_datasets_convert.py
---------------------------
Converts the survey datasets CSV (``datasets_summary.csv``) into a LaTeX
table grouped by ``Pages`` (MP, SP, MP+SP).  Mirrors the structure used by
``summary_tables_convert.py`` so the rendered table fits the same survey
preamble.

Usage:
    python3 summary_datasets_convert.py [input.csv] [output.tex]

Defaults:
    input  -> datasets_summary.csv
    output -> datasets_summary.tex
"""

import re
import sys
from pathlib import Path
import pandas as pd

# -- CLI args -----------------------------------------------------------------
BASE_DIR = Path(__file__).resolve().parent
INPUT_CSV = sys.argv[1] if len(sys.argv) > 1 else str(BASE_DIR / "datasets" / "datasets_summary.csv")
OUTPUT_TEX = sys.argv[2] if len(sys.argv) > 2 else str(BASE_DIR / "datasets" / "datasets_summary.tex")

DISPLAY_NAME_MAP = {
    "Name": "Dataset",
    "Pages": "Pages",
    "Type": "Type",
    "Venue": "Venue",
    "Domain": "Domain",
    "# Documents": "\\#\\,Docs",
    "Avg # Pages": "Avg\\,Pages",
    "Num Q/A": "\\#\\,Q/A",
    "Multi/Single Hop": "Hop",
}

COLUMN_SPEC_MAP = {
    "Name": "l",
    "Pages": "c",
    "Type": "c",
    "Venue": "l",
    "Domain": "l",
    "# Documents": "r",
    "Avg # Pages": "r",
    "Num Q/A": "r",
    "Multi/Single Hop": "c",
}

# Columns always hidden.
ALWAYS_HIDDEN = {"ID", "Bibtex"}

PREFERRED_ORDER = [
    "Name",
    "Pages",
    "Type",
    "Venue",
    "Domain",
    "# Documents",
    "Avg # Pages",
    "Num Q/A",
    "Multi/Single Hop",
]

# Normalise Type values so the column is consistent.
TYPE_NORMALISE = {
    "B": "BM",
    "Benchmark": "BM",
    "BENCHMARK": "BM",
    "PT+BM": "PT+BM",
    "PT+IT/FT": "PT+IT/FT",
    "IT/FT": "IT/FT",
    "BM": "BM",
    "PT": "PT",
}

PREAMBLE = r"""% -----------------------------------------------------------------------------
% Auto-generated by summary_datasets_convert.py -- do NOT edit manually.
% Re-generate:  python3 summary_datasets_convert.py datasets_summary.csv datasets_summary.tex
%
% Required packages (add to your preamble):
%   \usepackage{booktabs}
%   \usepackage{array}
%   \usepackage{xcolor}
%   \usepackage{colortbl}
%   \usepackage{caption}
%   \usepackage{adjustbox}
%   \usepackage{natbib}      % for \citeyear  (or use biblatex)
% -----------------------------------------------------------------------------

\definecolor{sectionbg}{gray}{0.88}

"""


def make_column_config(df_columns, hidden_cols):
    """Infer the displayed columns, labels, and column specs from the CSV."""
    visible = [col for col in df_columns if col not in hidden_cols]
    ordered = [col for col in PREFERRED_ORDER if col in visible]
    ordered.extend(col for col in visible if col not in ordered)
    return [
        (col, DISPLAY_NAME_MAP.get(col, col), COLUMN_SPEC_MAP.get(col, "l"))
        for col in ordered
    ]


def escape_latex(val):
    """Escape special LaTeX characters in a cell value."""
    if pd.isna(val):
        return "--"
    val = str(val).strip()
    if val in ("-", ""):
        return "--"
    for char, repl in [
        ("\\", r"\textbackslash{}"),
        ("&", r"\&"),
        ("%", r"\%"),
        ("$", r"\$"),
        ("#", r"\#"),
        ("_", r"\_"),
        ("{", r"\{"),
        ("}", r"\}"),
        ("~", r"\textasciitilde{}"),
        ("^", r"\textasciicircum{}"),
    ]:
        val = val.replace(char, repl)
    return val if val else "--"


def extract_year(bibtex_key):
    """Pull a 4-digit year out of a bibtex key, e.g. 'wang2025foo' -> '2025'."""
    if not bibtex_key or pd.isna(bibtex_key):
        return None
    m = re.search(r"(20\d{2}|19\d{2})", str(bibtex_key))
    return m.group(1) if m else None


def make_dataset_cell(row, name_col):
    r"""
    Format:  Name~(\citeyear{key})
    \citeyear requires natbib or biblatex; it prints just the year as a link.
    Falls back to plain (un-bolded) name if no bibtex / year available.
    """
    name = escape_latex(row[name_col])
    bibtex = str(row["Bibtex"]).strip() if pd.notna(row.get("Bibtex")) else ""
    if not bibtex:
        return name
    year = extract_year(bibtex)
    if year:
        return rf"{name}~(\citeyear{{{bibtex}}})"
    return rf"{name}~\cite{{{bibtex}}}"


def normalise_type(val):
    if pd.isna(val):
        return "--"
    s = str(val).strip()
    return TYPE_NORMALISE.get(s, s)


def build_table_rows(df, columns, name_col):
    lines = []
    for _, row in df.iterrows():
        cells = []
        for col, _, _ in columns:
            if col == name_col:
                cells.append(make_dataset_cell(row, name_col))
            elif col == "Type":
                cells.append(escape_latex(normalise_type(row.get(col, ""))))
            else:
                cells.append(escape_latex(row.get(col, "")))
        lines.append("  " + " & ".join(cells) + r" \\")
    return "\n".join(lines)


PAGES_SORT_ORDER = ["SP", "MP", "MP+SP"]
PAGES_SORT_RANK = {v: i for i, v in enumerate(PAGES_SORT_ORDER)}


def main():
    df = pd.read_csv(INPUT_CSV)

    if "Name" not in df.columns:
        raise ValueError("CSV is missing required 'Name' column.")
    if "Bibtex" not in df.columns:
        raise ValueError("CSV is missing required 'Bibtex' column.")
    if "Pages" not in df.columns:
        raise ValueError("CSV is missing required 'Pages' column.")

    # Stable sort by Pages: SP -> MP -> MP+SP, preserving original order within groups.
    df = df.copy()
    df["_pages_rank"] = (
        df["Pages"].fillna("").astype(str).str.strip()
        .map(PAGES_SORT_RANK)
        .fillna(len(PAGES_SORT_RANK) + 1)
    )
    df = df.sort_values(by=["_pages_rank"], kind="stable").drop(columns=["_pages_rank"])

    columns = make_column_config(df.columns, ALWAYS_HIDDEN)
    col_spec = " ".join(spec for _, _, spec in columns)
    col_headers = " & ".join(rf"\textbf{{{hdr}}}" for _, hdr, _ in columns)

    body = build_table_rows(df, columns, "Name")

    caption = (
        r"Datasets referenced in the MP-VRDU survey. "
        r"\textbf{Pages}: document scope (MP\,=\,Multi-Page, SP\,=\,Single-Page, "
        r"MP+SP\,=\,Mixed). "
        r"\textbf{Type}: dataset role -- "
        r"PT (Pretraining), IT/FT (Instruction Tuning / Fine-Tuning), "
        r"BM (Benchmark), and combinations. "
        r"\textbf{Hop}: question-hop type (Single, Multi, or both). "
        r"`--' indicates not reported."
    )

    table_lines = [
        r"% -- tab:dataset_survey -----------------------------------------------",
        r"\begin{table*}[t]",
        r"\centering",
        r"\scriptsize",
        r"\captionsetup{font=small}",
        r"\setlength{\tabcolsep}{3pt}",
        r"\renewcommand{\arraystretch}{0.86}",
        r"\begin{adjustbox}{width=\textwidth}",
        rf"\begin{{tabular}}{{{col_spec}}}",
        r"\toprule",
        rf"{col_headers} \\",
        r"\midrule",
        "",
        body,
        "",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{adjustbox}",
        rf"\caption{{{caption}}}",
        r"\label{tab:dataset_survey}",
        r"\end{table*}",
        "",
    ]

    with open(OUTPUT_TEX, "w", encoding="utf-8") as f:
        f.write(PREAMBLE)
        f.write("\n".join(table_lines))
    print(f"OK  Wrote {OUTPUT_TEX}  ({len(df)} rows, ungrouped)")


if __name__ == "__main__":
    main()
